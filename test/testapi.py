import requests
import os
# api_url = "https://www.dmxapi.cn/v1/responses"  # openai新的替换/chat/completions的API 

"""
╔═══════════════════════════════════════════════════════════════╗
║                  DMXAPI GPT-5系列及其他模型调用示例             ║
╚═══════════════════════════════════════════════════════════════╝

📝 功能说明:
   本脚本演示如何使用 OpenAI SDK 调用 DMXAPI 的 GPT-5系列及其他模型
   通过 responses.create 接口进行对话交互（流式输出）

═══════════════════════════════════════════════════════════════
"""

from openai import OpenAI
from dotenv import load_dotenv
load_dotenv()  


# ═══════════════════════════════════════════════════════════════
# 🔑 步骤1: 初始化 DMXAPI 客户端
# ═══════════════════════════════════════════════════════════════

client = OpenAI(
    api_key=os.getenv("api_key"),
    base_url="https://www.dmxapi.cn/v1",
)

# ═══════════════════════════════════════════════════════════════
# 💬 步骤2: 创建流式对话请求
# ═══════════════════════════════════════════════════════════════

response = client.responses.create(
    # 🤖 模型选择
    model="gpt-4.1-nano", # 选个最便宜的

    # 💭 用户输入
    # 您想要向模型提出的问题或对话内容
    # (gpt-5-codex 如果跑不通的话，需要改成结构化的数组格式)
    input="你好!",

    # 🔄 流式输出开关
    # True: 实时接收响应，边生成边输出
    stream=True,

    # ---------- 输出控制参数 ----------
    # 📏 最大输出令牌数 (范围: 1 - 128000)
    # 控制模型响应的最大长度
    # 💡 提示: 实际输出可能少于此值
    # max_output_tokens=128000,

    # ---------- 采样参数 ----------
    # 🌡️ 温度参数 (范围: 0.0 - 2.0)
    # 控制输出的随机性和创造性:
    #   • 较低值 (0.0-0.3): 输出更确定、更一致,适合事实性任务
    #   • 中等值 (0.4-0.8): 平衡创造性和一致性
    #   • 较高值 (0.9-2.0): 输出更随机、更有创意
    # ⚠️ 建议: 只调整 temperature 或 top_p 其中之一
    # temperature=1,

    # 🎯 核采样参数 (范围: 0.0 - 1.0)
    # 控制采样的概率质量阈值:
    #   • 0.1: 只考虑概率最高的 10% 的词
    #   • 0.5: 只考虑概率最高的 50% 的词
    #   • 1.0: 考虑所有可能的词
    # 💡 提示: 较低的值会让输出更集中,较高的值会增加多样性
    # ⚠️ 建议: 只调整 temperature 或 top_p 其中之一
    # top_p=1,

    # ---------- 推理配置 ----------
    # 🧠 推理配置选项 (仅适用于 GPT-5 和 O 系列推理模型)
    # reasoning={
    #     "effort": "none"    # 推理精力等级
    # },
)

# ═══════════════════════════════════════════════════════════════
# 📤 步骤3: 处理流式响应
# ═══════════════════════════════════════════════════════════════

try:
    # 遍历流式响应事件
    for event in response:
        # 检查事件类型是否为文本增量输出
        if event.type == "response.output_text.delta":
            # 实时打印文本内容 - 不换行,立即刷新输出缓冲区
            print(event.delta, end="", flush=True)

except KeyboardInterrupt:
    # 处理用户中断 - 当用户按 Ctrl+C 时优雅退出
    print("\n\n⚠️ 用户中断了请求")

except Exception as e:
    # 处理其他异常 - 捕获并显示任何意外错误
    print(f"\n\n❌ 发生错误: {e}")

# 最后换行 - 确保输出格式整洁
print()